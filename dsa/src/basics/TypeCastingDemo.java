package basics;

public class TypeCastingDemo {

    public static void main(String[] args) {

        // Implicit Typecasting
        int num = 65;

        char ch = (char) num;

        System.out.println(ch);

        double d = 10;
        System.out.println(d);

        /*
         Implicit Typecasting:
         smaller -> bigger

         Explicit Typecasting:
         bigger -> smaller
        */
    }
}
